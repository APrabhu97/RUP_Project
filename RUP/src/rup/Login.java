private boolean validate_login(String username,String password) {
   try{          //Connection Code 
       Class.forName("com.mysql.jdbc.Driver");  // MySQL database connection
       Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/rup_project?zeroDateTimeBehavior=convertToNull","root","robin");
                                                                    
       PreparedStatement pst =   conn.prepareStatement("Select * from login where username = '"
                                +username+"' and password = '"+password+"'");
       ResultSet rs = pst.executeQuery();    
       //conn.close();
       if(rs.next())            
           return true;    
       else
           return false;            
   }
   catch(Exception e){
       e.printStackTrace();
       return false;
   }       
}
